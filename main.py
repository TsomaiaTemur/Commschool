class Calculator:
    def __init__(self):
        pass

    @classmethod
    def add(cls,a,b):
        return a+b

    @classmethod
    def multiply(cls,a,b):
        return a*b

    @classmethod
    def subtract(cls,a,b):
        return a-b

    @classmethod
    def divide(cls,a,b):
        return a/b